      if (localStorage.getItem(this.key) != null)
        this.value = JSON.parse(localStorage.getItem(this.key));
      console.log("Loading data from local storage");
    },
    save: function () {
      localStorage.setItem(this.key, JSON.stringify(this.value));
    },
